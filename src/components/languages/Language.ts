// Alphabetically sorted version
export const languagesSorted = [...languages].sort((a, b) => 
  a.name.localeCompare(b.name)
);

// Helper function to get language by code
export const getLanguageByCode = (code: string): Language | undefined => {
  return languages.find(lang => lang.code === code);
};

// Helper function to get language names only (for simple dropdowns)
export const getLanguageNames = (): string[] => {
  return languages.map(lang => lang.name);
};